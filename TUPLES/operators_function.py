myTuple = (1,2,3,4,5)
myTuple1 = (6,7,8,9,10)

print(myTuple + myTuple1)
print(myTuple * 4)

print(4 in myTuple) # search element

# methods in tuple
print(myTuple.count(2))
print(myTuple.index(4))

# functions in tuple
print(len(myTuple1))
print(max(myTuple1))
print(min(myTuple1))

# list to tuple method
print(tuple([1,2,3,4]))
