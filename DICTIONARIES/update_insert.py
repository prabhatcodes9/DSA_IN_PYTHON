# Update
my_dict = {'name':'Eddy', 'age':'28'}
my_dict['age'] = 30
my_dict['name'] = 'Prabhat'
print(my_dict) # Time complexity : O(1)
               # Space complexity : O(n)

# Insert
my_dict1 = {'name':'Eddy', 'age':'28'}
my_dict1['address'] = 'London'
print(my_dict1)  # Time complexity : O(1)
               # Space complexity : O(1)